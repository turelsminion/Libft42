#include "libft.h"

int	ft_atoi(char *str)
{
	long int	n;
	int			neg;

	while (*str == '\r' || *str == '\t' || *str == '\n' || *str == '\f'
		|| *str == '\v' || *str == ' ')
		str++;
	neg = *str == '-';
	if ((n = 0) || (*str == '-') || (*str == '+'))
		str++;
	while (*str && (*str >= '0' && *str <= '9'))
	{
		if (n > 2147483647 && neg == 0)
			return (-1);
		if (n > 2147483648 && neg == 1)
			return (0);
		n = n * 10 + (*(str++) - '0');
	}
	return (n * (1 + -2 * neg));
}
