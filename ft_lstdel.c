#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *next;
	t_list *list;

	list = *alst;
	while (list)
	{
		next = list->next;
		del(list->content, list->content_size);
		free(list);
		list = next;
	}
	*alst = NULL;
}
