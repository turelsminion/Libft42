#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb < 0 && nb > -2147483648)
	{
		nb *= -1;
		ft_putchar('-');
	}
	if (nb == -2147483648)
		ft_putstr("-2147483648");
	else if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
